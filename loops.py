# simple program to test 'for' loop

groceries=['milk', 'eggs', 'egg whites', 'pizza', 'coke', 'juice', 'salsa']
print("the grocery list will be printed using a for loop: \n")
for item in groceries:
    # we can use groceries(x:y) to splice up the list
    # where x stands for the starting position and y for the ending position
    print(item)

print('\n')

supplies=['tools', 'hardware']
print("the supplies list will be printed using a for loop too: \n")
for equipment in supplies:
    print(equipment)